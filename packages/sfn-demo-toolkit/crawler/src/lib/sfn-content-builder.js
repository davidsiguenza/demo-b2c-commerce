/**
 * Convert the crawler's analysis JSON into the BrandContent shape used by
 * src/extensions/branding/types.ts in patched SFN repos.
 *
 * Crawler output (analysis.content):
 *   {
 *     hero: { slide1, slide2, slide3, slide4 },
 *     featuredContent: { women, men },
 *     newArrivals, categoryGrid, pageTitle, pageDescription
 *   }
 *
 * Target BrandContent:
 *   {
 *     id, displayName, logo: { src, alt },
 *     hero: BrandHeroSlide[],
 *     featuredCards: { primary: BrandFeaturedCard[], textOnly?: BrandFeaturedCard }
 *   }
 */

function slideToBrandHeroSlide(slide, idx, fallbackLink) {
    if (!slide) return null;
    return {
        id: `slide-${idx + 1}`,
        title: slide.title || '',
        subtitle: slide.subtitle || undefined,
        imageUrl: slide.imageUrl || '',
        imageAlt: slide.imageAlt || slide.title || '',
        ctaText: slide.ctaText || undefined,
        ctaLink: slide.ctaLink || fallbackLink || '/category/root',
    };
}

function featuredToCard(featured, fallbackLink) {
    if (!featured) return null;
    return {
        title: featured.title || '',
        description: featured.description || '',
        imageUrl: featured.imageUrl || undefined,
        imageAlt: featured.imageAlt || featured.title || undefined,
        ctaText: featured.ctaText || undefined,
        ctaLink: featured.ctaLink || fallbackLink || '/category/root',
    };
}

export function buildBrandContent(analysis) {
    const fallbackLink = '/category/root';
    const content = analysis.content ?? {};

    const heroSlides = ['slide1', 'slide2', 'slide3', 'slide4']
        .map((k, i) => slideToBrandHeroSlide(content.hero?.[k], i, fallbackLink))
        .filter((s) => s && s.imageUrl);

    const womenCard = featuredToCard(content.featuredContent?.women, '/category/womens');
    const menCard = featuredToCard(content.featuredContent?.men, '/category/mens');
    const primary = [womenCard, menCard].filter(Boolean);

    let textOnly;
    if (content.categoryGrid?.title) {
        textOnly = {
            title: content.categoryGrid.title,
            description: content.categoryGrid.description || `Welcome to ${analysis.displayName}.`,
        };
    }

    return {
        id: analysis.brandId,
        displayName: analysis.displayName,
        logo: {
            src: analysis.images?.logo || '',
            alt: analysis.displayName,
        },
        hero: heroSlides,
        featuredCards: {
            primary,
            ...(textOnly ? { textOnly } : {}),
        },
    };
}

/**
 * Render BrandContent as a TypeScript module string suitable for
 * src/extensions/branding/clients/<id>/content.ts.
 */
export function renderBrandContentModule(brandContent) {
    const stringify = (v) => JSON.stringify(v, null, 4).replace(/^/gm, '    ').trimStart();
    return `/**
 * Auto-generated by @davidsiguenza/sfn-demo-toolkit.
 * Edit by hand if you need fine-tuning, but re-running brand will overwrite.
 */
import type { BrandContent } from '../../types';

const content: BrandContent = ${stringify(brandContent)};

export default content;
`;
}

/**
 * Render the per-client theme.css content from a token bundle.
 * Tokens shape mirrors the crawler's color extraction: { primary, primaryForeground, accent, accentForeground, background, foreground, border, ring }
 */
export function renderThemeCss(brandId, tokens) {
    const map = {
        primary: '--primary',
        primaryForeground: '--primary-foreground',
        accent: '--accent',
        accentForeground: '--accent-foreground',
        background: '--background',
        foreground: '--foreground',
        border: '--border',
        ring: '--ring',
    };
    const lines = [];
    for (const [k, cssVar] of Object.entries(map)) {
        if (tokens && tokens[k]) {
            lines.push(`    ${cssVar}: ${tokens[k]};`);
        }
    }
    if (!lines.length) {
        return `/* ${brandId} brand: no tokens extracted yet. Edit this file or rerun brand to populate. */\n:root[data-brand='${brandId}'] {\n}\n`;
    }
    return `/* ${brandId} brand tokens, auto-generated. */\n:root[data-brand='${brandId}'] {\n${lines.join('\n')}\n}\n`;
}

/**
 * Render an .env.profiles/<id>.env file content.
 */
export function renderEnvProfile(brandId, options = {}) {
    const lines = [
        `# ${brandId} profile, auto-generated by sfn-demo-toolkit.`,
        `# Switch with: pnpm demo:switch ${brandId}`,
        '',
        `PUBLIC__app__global__branding__activeClient=${brandId}`,
        '',
        '# Fill these in from your sandbox + SLAS Admin:',
        'PUBLIC__app__commerce__api__clientId=',
        'PUBLIC__app__commerce__api__organizationId=',
        'PUBLIC__app__commerce__api__shortCode=',
        'PUBLIC__app__commerce__api__privateKeyEnabled=true',
        'COMMERCE_API_SLAS_SECRET=',
        '',
        `PUBLIC__app__defaultSiteId=${options.siteId ?? ''}`,
        '',
    ];
    return lines.join('\n');
}
